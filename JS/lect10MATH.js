//MATH OBJECT

//Math.PI
console.log(Math.PI); //value of pie

//Math.round() (round value to its nearest integer)
let round = 100.248765;
let Round = 100.6745;
console.log(Math.round(round), Math.round(Round));

//Math.pow(x,y) (return the value of x to the power of y)
let Power = 5;
console.log(Math.pow(Power, 2), 5 ** 2);

//Math.sqrt() (return square root of x)
console.log(Math.sqrt(25));

//Math.abs() (return the absolute(positive) value of x)
console.log(Math.abs(-12.54));

//Math.ceil() (return the value of x rounded upto its nearest integer)
console.log(Math.ceil(5.41), Math.round(5.41));
console.log(Math.ceil(99.4), Math.round(99.41));

//Math.floor() (return the value of x rounded down to its nearest integer)
console.log(Math.floor(4.79), Math.round(4.79));
console.log(Math.floor(99.99), Math.round(99.99));

//Math.min() (use to find min value of the list)
console.log(Math.min(0, 2, 4, 9, 6, 1, -6, -20));

//Math.max() (use to find max value of the list)
console.log(Math.max(0, 2, 4, 9, 6, 1, -6, -20));

//Math.random() (random no between 0 & 1)
console.log(Math.floor(Math.random() * 100));

//Math.trunc() (returns the integer part of a no.)
console.log(Math.trunc(-4.6));




